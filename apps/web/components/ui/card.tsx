import * as React from 'react';
import { cn } from '@/lib/utils';

/**
 * Cursor 风格 Card — 薄边框、无阴影、无圆角过大
 */
export const Card = React.forwardRef<HTMLDivElement, React.HTMLAttributes<HTMLDivElement>>(
  ({ className, ...props }, ref) => (
    <div
      ref={ref}
      className={cn(
        'rounded-lg border border-[hsl(var(--color-border))] bg-[hsl(var(--color-card))] text-[hsl(var(--color-card-foreground))]',
        className,
      )}
      {...props}
    />
  ),
);
Card.displayName = 'Card';

